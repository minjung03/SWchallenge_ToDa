package com.cookandroid.to_da_project;

public class List {

    int num;
    String list_value;

    public List(int num, String list_value) {
        this.num = num;
        this.list_value = list_value;
    }

    public int getNum() {
        return num;
    }

    public void setNum(int num) {
        this.num = num;
    }

    public String getList_value() {
        return list_value;
    }

    public void setList_value(String list_value) {
        this.list_value = list_value;
    }
}
